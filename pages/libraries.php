  <p><strong>INTRODUCTION: PARTNERSHIP BETWEEN THE PROVINCIAL GOVERNMENT AND MUNICIPALITIES</strong></p>
  
  <p>The rendering of library services is based on a partnership between the Provincial Government and local authorities.  This partnership involves the following:</p>
  <ul>
    <li> The local authority manages buildings, staff and some of running costs for Libraries.</li>
	<li> The provincial library service is responsible for procurement and delivery of library stock, building of new library buildings, renovations and supply of other library material and is also responsible for support services.</li>
  </ul>
	 
  <p><strong>DEVELOPMENT OF LIBRARIES IN RUSTENBURG JURISDICTION AREA</strong></p>
  
  <p style ="text-align:justify;">The demarcation of municipalities after the 2000 elections resulted in the inclusion of vast rural and semi-urban populations in the area of the Rustenburg Local Municipality.  Library services in these areas are mostly non-existent and the population can be described as disadvantaged with low literacy levels.</p>
  
  <p style ="text-align:justify;">During the restructuring and transformation process of the Rustenburg Local Municipality it came forward that there is an oversupply of libraries in the central Rustenburg area, but that other areas, especially the less developed areas are without such facilities. There are 6 public/community libraries in a radius less than 10 km from the Central Business District (CBD). It was therefore decided to integrate some of these libraries and to establish new libraries in less developed areas.</p>
  
  <p style ="text-align:justify;">There are also community libraries in Boitekong, Marikana, Tlhabane and Phokeng. The library in Phokeng is managed in a partnership between the Rustenburg Local Municipality and the Royal Bafokeng Administration.</p>
  
  <p style ="text-align:justify;">All Libraries in exception of Barseba, Chaneng, Mathopestadt, Mamerotse are fully computerised.  It is envisaged that all these libraries would be linked to a local library network with global access.</p>
  
  <p style ="text-align:justify;">The provision of Library and Information Services should also be achieved within approved municipal library-by laws approved by the Council of Rustenburg Local Municipality. Revised municipal by-laws have as per item 220 of 26 November 2019 Council resolution been approved and are at promulgation stage. Once promulgated the by-laws will be uploaded on the website. The provision of services is also carried within approved annual tariffs and such tariffs are published on the municipal tariff book for the current financial year and will be updated as guided by legislation periodically.</p>
  
  <p><strong>VISION OF THE UNIT: LIBRARY AND INFORMATION MANAGEMENT</strong></p>
  
  <p style ="text-align:justify;">To provide in co-operation with the relevant governing bodies on local and provincial level, a dynamic library and information service that meets the educational, informational, cultural, economic, technological and recreational needs of the community at large.</p>
  
  <p><strong>MISSION OF THE UNIT:  LIBRARY AND INFORMATION MANAGEMENT</strong></p>
  
  <p style ="text-align:justify;">To render a dynamic library and information service to the community in order to improve their quality of life by supporting the aims of the Integrated Development Plan and to supply information resources, educational media, literacy support, opportunities for life-long learning, cultural expression, recreation and other fields of human endeavour.</p>
  
  <p><strong>CORE BUSINESS, OBJECTIVES AND KEY PERFORMANCE AREAS OF THE UNIT:  LIBRARY AND INFORMATION SERVICES</strong></p>
  
  <p><strong>Core business</strong></p>
  
  <p style ="text-align:justify;">Provision and management of library and information services for Rustenburg to ensure community access to these services for academic, business and personal development purposes.</p>
  
  <p><strong>Objectives</strong></p>
  
  <p>The Library and Information Management Unit focuses on the following objectives to contribute to the success of the IDP:</p>
  <ul>
    <li> To determine the needs of the community by public participation processes such as the collecting and analysing of data relating to the needs of individuals and groups (e g children, young adults, students, adults, professional people, etc) in the community.</li>
	<li> To extend library services by establishing branch or satellite libraries in unserved areas with funding from the Provincial Government, the Local Municipality and donors.</li>
	<li> To obtain external funds for the establishment of an information and educational network and rendering of excellent Library and Information services in the jurisdiction area of the local municipality</li>
	<li> To build and maintain a community information database and disseminating information to the community.</li>
	<li> To promote library services and instil a reading culture, improve literacy levels and provide free and open access to community and survival information.</li>
	<li> To present programmes such as story hours, talks, life skills training courses, literacy training, etc to promote reading and literature and to contribute to the upliftment of the community.</li>
  </ul>
	 
  <p><strong>Key Performance Areas</strong></p>
  <ul>
  
         <li>    Management of library and information services</li>
		 <li>	 Development of library collections</li>
		 <li>	 Provision of library lending services</li>
		 <li>    Provision of reference and information services</li>
		 <li>	 Marketing of library and information services and user education</li>
		 <li>	 Presenting of skills training and outreach programmes</li>
		 <li>    Conducting of research and surveys and evaluation of statistics and standards</li>
  </ul>
  
